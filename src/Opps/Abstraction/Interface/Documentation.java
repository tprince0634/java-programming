package Opps.Abstraction.Interface;

public class Documentation {

/*

INTERFACE IN JAVA

An interface is a blueprint of a class used to achieve 100% abstraction.

It can contain:
✔ Abstract methods                        - By default: public abstract
✔ Default methods (Java 8+)             (concrete method)  -By default: public default
✔ Static methods (Java 8+)
✔ Constants                                (public static final)


void sound();             // abstract
default void sleep() {}   // default concrete
static void info() {}     // static concrete

If default keyword didn’t exist, you couldn't distinguish abstract vs concrete easily.



It cannot contain:
✘ Constructors
✘ Instance variables
*/


}
