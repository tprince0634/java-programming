package Opps.Encapsulation;

public class Documentation {
    /**
     * WHAT IS ENCAPSULATION
     *
     * Encapsulation is one of the four fundamental Object-Oriented Programming (OOP) concepts,
     * along with inheritance, polymorphism, and abstraction.
     * It is the mechanism of wrapping the data (variables) and the code (methods) together
     * as a single unit.
     * In Java, encapsulation is implemented using classes and objects
     * to bundle the data (fields) and methods that operate on the data into a single unit. (the class).
     *
     *This ensures that the data is not directly manipulated from outside the class,
     *  but through controlled access points (methods).
     *
     * Declare the variables of a class as private so they can't be directly accessed outside the class.
     * Provide public getter and setter methods to allow access and modification of the private fields.
     *  */




}
