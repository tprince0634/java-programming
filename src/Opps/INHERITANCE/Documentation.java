package Opps.INHERITANCE;

public class Documentation {

    /**
     * Inheritance in Java
     * Inheritance is a fundamental concept in object-oriented programming (OOP) that allows one class to inherit
     *  the properties and behavior of another class. In Java, inheritance is implemented using the extends keyword.
     */
//    THERE ARE FOUR TYPES OF INHERITANCE;

    /**
     * SINGLE INHERITANCE
     * In single inheritance, a subclass inherits the properties and behavior of a single superclass.
     * */


    /**
     * Multilevel inheritance
     * It is a type of inheritance in object-oriented programming
     * where a class inherits from another class that itself inherits from another class.
     *                 OR
     *  where a class (child class) inherits from another class (parent class),
     *  which in turn inherits from another class (grandparent class).
     */

    /**
     * Hierarchical inheritance
     * //In hierarchical inheritance, multiple subclasses inherit from a single superclass.
     * */

    /**
     * Multiple inheritance
     * where a class can inherit features  from more than one superclass.
     * However, Java does not support multiple inheritance with classes to avoid complexity and ambiguity,
     * particularly the "Diamond Problem."
     * Instead, Java allows multiple inheritance through interfaces.
     * */

    /**
     * Hybrid Inheritance is a type of inheritance in object-oriented programming that combines
     * two or more types of inheritance, such as single inheritance, multiple inheritance,
     * and multilevel inheritance.
     */




}
