package Collection.Queue.Extends.Deque.Implementations;

public class DequeMethods {
    /***
     * 🔹 Insertion Methods
     * Method	Description
     * addFirst(E e)	Inserts element at the front
     * addLast(E e)	Inserts element at the back
     * offerFirst(E e)	Inserts at front, returns false on failure
     * offerLast(E e)	Inserts at back, returns false on failure
     *
     * 🔹 Removal Methods
     * Method	Description
     * removeFirst()	Removes and returns element at front
     * removeLast()	Removes and returns element at back
     * pollFirst()	Removes and returns front, returns null if empty
     * pollLast()	Removes and returns back, returns null if empty
     *
     * 🔹 Peek (Read) Methods
     * Method	Description
     * getFirst()	Returns (but doesn't remove) front element; throws exception if empty
     * getLast()	Returns (but doesn't remove) last element; throws exception if empty
     * peekFirst()	Returns front element, or null if empty
     * peekLast()	Returns last element, or null if empty
     *
     * 🔹 Stack-like Methods (LIFO)
     * Method	Equivalent
     * push(E e)	Same as addFirst()
     * pop()	Same as removeFirst()
     * peek()	Same as peekFirst()
     */


}


