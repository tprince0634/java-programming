package Collection.Comparable2;

public class Document {

/*Comparable:
The Comparable interface is used to define the natural ordering of objects.
A class that implements Comparable must override the compareTo method.
This method compares the current object with the specified object for order.
*/

/*The compareTo method returns:
A negative integer if the current object is less than the specified object.
Zero if the current object is equal to the specified object.
A positive integer if the current object is greater than the specified object.
*/


/*✅ Comparable vs Comparator: Method Syntax
Interface	Syntax	When to Use
Comparable	Collections.sort(list) or list.sort(null)	When class implements Comparable
Comparator	list.sort(new ClassName())	When using external custom sorting logic*/
}

