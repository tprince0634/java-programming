package ThisKeyword;

public class Documentation {
    /*
    Purpose of this Keyword:
   1. To Refer to Instance Variables:
    When a local variable or parameter has the same name as an instance variable, this can be used to refer to the instance variable.
   2. To Call Another Constructor:
    Within a constructor, this() can be used to call another constructor in the same class.
   3. To Pass the Current Object as a Parameter:
    It can be used to pass the current object as a parameter to another method or constructor
   4. To Return the Current Object:
   It can be used to return the current object from a method, enabling method chaining.
   */
}
