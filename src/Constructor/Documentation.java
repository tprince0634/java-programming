package Constructor;

public class Documentation {
    /*
    * A constructor in Java is a special type of method that is automatically called when an instance
    *  (object) of a class is created. It is used to initialize the newly created object and set
    * its initial state. Constructors have the same name as the class and do not have a return
    * type, not even void.*/

    /*/**
     * DEFAULT CONSTRUCTOR
     * A default constructor is a constructor that has no parameters.
     *  It is used to initialize an object with default values.
     *  When an object is created without passing any arguments,
     *  the default constructor is called.
     *
     *  In Java, a default constructor is a special constructor that is created by the Java compiler if you will
     *  not create then complier will create with default values
     */


    /**
     * PARAMATERIZED CONSTRUCTOR
     * A parameterized constructor is a constructor that has one or more parameters.
     *  It is used to initialize an object with specific values.
     *  When an object is created with arguments, the parameterized constructor is called.
     */

    /* Copy Constructor
    A copy constructor is a constructor that initializes an object by copying the state
    of another object of the same class.*/

}
