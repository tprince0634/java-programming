package Wrapper;

public class Documentation {
/**
 * In Java, a wrapper class is a class that encapsulates (or "wraps") a primitive data type  and convert into an object.
    This allows primitive types to be treated as objects.

 In Java, the primitive data types (e.g., int, double, boolean) are not objects, which means they cannot
 be used in scenarios where objects are required, such as collections like ArrayList or methods that
 expect objects. Wrapper classes are a set of classes provided by Java to represent each of the primitive
 data types as objects. These classes provide utility methods and enable primitive types to be used
 in contexts where objects are required.

 int num = 10; // Primitive type
 Integer obj = Integer.valueOf(num);                            // Primitive to Wrapper (Boxing)
 int value = obj.intValue();                                    // Wrapper to Primitive (Unboxing)

 Boxing is the process of converting a primitive type to its corresponding wrapper class.
 Unboxing is the process of converting a wrapper class back to its primitive type.




 */
}
