package Super;

public class Documentation {

    /*
super refers to the immediate parent class object.
It can be used only in inheritance (extends).
super is used to access parent class variables when names clash.
super.method() calls a parent class instance method.
Method overriding is NOT mandatory to use super.method().
super() is used to call the parent class constructor.
super() must be the first statement inside a constructor.
super cannot be used in static context (including main).
super cannot access private or static members of parent.
super always refers to only the immediate parent, not grandparent.
    */
}
