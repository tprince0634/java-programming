package String_Buffer_Builder;

public class Documentation {

    /*String:
    * It is a  immutable sequence of characters.
    * Once a String object is created, its value cannot be modified.
    * Any operation that appears to modify a String actually creates a new String object with the modified value.
    * String objects are thread safe for sharing across different threads without synchronization overhead.
    * */


    /*StringBuilder:
    * StringBuilder: is a mutable sequence of characters. However, it is not inherently thread-safe.
    * StringBuilder is recommended for use in single-threaded environments or when synchronization is not required.
    * Method Chaining (thread safe) also
    * */


    /*StringBuffer:
    * StringBuffer is a mutable sequence of characters, designed for use in multi-threaded environments.
    * StringBuffer provides synchronized methods for appending, inserting, and modifying characters, making it thread-safe.
    * Use StringBuffer when you need a mutable sequence of characters in a multi-threaded environment.
     */



}
