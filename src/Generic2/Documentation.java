package Generic2;

public class Documentation {

    /* Generics in Java is a feature that allows you to define classes,
     interfaces, and methods with a placeholder for types, enabling you
     to create code that is type-safe and reusable.
     (A placeholder in Java Generics is a temporary name for a data type )*/


/*
* A generic type parameter is a feature in many programming languages
*  that allows you to write code that can operate on objects of various
* types while maintaining strong type safety.
*
*  Generics enable you to create classes, methods, and data structures where
*  the exact type of the data they operate on is specified later, at the point of use,
*  rather than at the point of definition.
 */





    /**
     *  For example, a generic class can work with any type specified by the user,
     *  and that type can be enforced at compile time.
     *
     *  class ClassName<T> {
     *     // Class body
     * }
     *
     * Where T is the type parameter, which can be any valid identifier.
     * q
     * Conventionally, single-letter names are used for type parameters, such as
     *
     * T for Type,
     * E for Element      Used in Collections
     * K for Key          (Used in Maps)
     * V for Value        (Used in Maps)
     * Numbers
     * etc
     *
     */

}


