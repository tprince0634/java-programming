package Exception_Handling.Throwable.Exception.Checked;

public class Documentation {
    /**
     *  -->> Checked exceptions are exceptions that are checked at compile-time.
     *  The Java compiler requires that these exceptions be either caught in a try-catch block
     *  or declared in the method signature using the throws keyword.
     *  This ensures that the programmer is aware of these exceptions and has a plan to handle them.

                       Exception
     *               ├── Checked Exceptions      (it is a subclass of Exceptions )
     *               │   ├── IOException
     *               │   ├── SQLException
     *               │   └── ClassNotFoundException
     */
}

