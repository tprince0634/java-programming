/*
A lambda expression is a short block of code that takes in parameters and returns a value.
It's a concise way to represent an anonymous function—that is, a function without a name.

Lambda expressions are often used to implement functional interfaces—interfaces with a
single abstract method.
*/


/* A FunctionalInterface in Java is an interface that has exactly one abstract method,
   and it may have multiple default or static methods.
   It is used to represent a single functionality or behavior and is typically used with
   lambda expressions or method references.

   In Java, the @FunctionalInterface annotation is not required,
   but it is used to indicate that the interface is intended to be functional,
   making it easier to catch any accidental addition of more than one abstract method.

   */
